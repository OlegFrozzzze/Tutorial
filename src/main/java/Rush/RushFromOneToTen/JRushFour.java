package Rush.RushFromOneToTen;

public class JRushFour {
    int age;
    String name;
    char sex;

    public JRushFour(String name) {
        this.name = name;
        this.age = 1;
    }
    public JRushFour(String name, int age) {
        this.name = name;
        this.age = age;
    }
    public JRushFour(String name, int age, char sex) {
        this.name = name;
        this.age = age;
        this.sex = sex;
    }
}

